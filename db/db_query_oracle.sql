
CREATE TABLE MFS_TXN_TYPE (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  CORE_TXN_TYPE_ID NUMBER NOT NULL,
  TXN_CODE NUMBER ,
  NAME NVARCHAR2(200) NOT NULL,
  DESCRIPTION NVARCHAR2(200) ,
  IS_ACTIVE NUMBER,
  CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "PRIMARY_KEY_MFS_TXN_TYPE" PRIMARY KEY ("ID")
);

CREATE INDEX "MFS_TXN_TYPE_NAME_IDX" ON "MFS_TXN_TYPE" ("NAME");

CREATE TABLE MFS_GROUP_CONFIG (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  GROUP_ID NUMBER NOT NULL,
  NAME NVARCHAR2(200) NOT NULL, -- ENUM CUSTOMER/MERCHANT
  DESCRIPTION NVARCHAR2(200) ,
  IS_ACTIVE NUMBER ,
  CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "PRIMARY_KEY_MFS_GROUP_CONFIG" PRIMARY KEY ("ID")
);

CREATE INDEX "MFS_GROUP_CONFIG_NAME_IDX" ON "MFS_GROUP_CONFIG" ("NAME");

CREATE TABLE TXN_LIMIT_CONFIGS (
 ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  WALLET_HOLDER NVARCHAR2(10) NOT NULL, -- ENUM FROM/TO
  TXN_TYPE NVARCHAR2(200) NOT NULL, -- ENUM AGENT_POINT_CASH_IN/AGENT_POINT_CASHOUT
  DESCRIPTION NVARCHAR2(200) ,
  DAILY_LIMIT NUMBER(19,6) ,
  MONTHLY_LIMIT NUMBER(19,6) ,
  MIN_AMOUNT NUMBER(19,6) ,
  MAX_AMOUNT NUMBER(19,6) ,
  DAILY_LIMIT_COUNT NUMBER NOT NULL,
  MONTHLY_LIMIT_COUNT NUMBER NOT NULL,
  IS_ENABLE NUMBER ,
  CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  LAST_MODIFIED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "PRIMARY_KEY_TXN_LIMIT_CONFIG" PRIMARY KEY ("ID")
);
CREATE INDEX "TXN_LIMIT_CONFIGS_TXN_TYPE_IDX" ON "TXN_LIMIT_CONFIGS" ("TXN_TYPE");

CREATE TABLE MFS_LEDGER_ACCOUNTS (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  ACCOUNT_ID NUMBER NOT NULL, -- SYSTEM ACCOUNT_ID , FOR MEMBER ACCOUNTTYPEID
  CODE NVARCHAR2(200) UNIQUE NOT NULL, -- ENU
  DESCRIPTION VARCHAR(200) ,
  PARENT_ID NUMBER,
  IS_ACTIVE NUMBER,
  IS_ONLY_PARENT NUMBER,
  LEDGER_LEVEL NUMBER,
  CATEGORY NVARCHAR2(20) ,
  TYPE NVARCHAR2(20) ,
  CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  LAST_MODIFIED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "PRIMARY_KEY_MFS_LEDGER_AC" PRIMARY KEY ("ID")
);

